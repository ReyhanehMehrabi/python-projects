# For loop

five_a_day = ["Apple", "Banna", "Cherry", "Mango", "PineApple"]

five_a_day[-1]  # PineApple
five_a_day[0]  # Apple

for fruit in five_a_day:
    print(fruit)

for letter in "PineApple":  # a string in python is a list of characters
    print(letter)

for item in range(0, 6):
    print(item)
